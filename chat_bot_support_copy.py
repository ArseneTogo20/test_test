# Fonction pour obtenir une réponse basée sur l'entrée de l'utilisateur
def respond(user_input):
    user_input = user_input.lower()
    
    # Parcourir les intentions pour trouver une correspondance
    for intent in intents:
        # Currently, we only have a fallback intent
        return random.choice(intent['responses'])
    
    return random.choice(intents[-1]['responses'])

def main():
    st.title("LE BOT")
    
    # Initialiser l'historique des messages
    if 'messages' not in st.session_state:
        st.session_state.messages = []  # Initialisation de messages

    # Afficher l'historique des messages
    for msg in st.session_state.messages:
        st.write(msg)

    # Entrée utilisateur
    user_input = st.text_input("Vous :", "")
    
    if st.button("Envoyer"):
        if user_input:
            # Ajouter le message de l'utilisateur à l'historique
            st.session_state.messages.append(f"You: {user_input}")
            
            # Obtenir la réponse du bot
            response = respond(user_input)
            
            # Afficher la réponse du bot
            st.session_state.messages.append(f"Bot: {response}")
            
            # Effacer l'entrée utilisateur
            st.session_state.messages.append("")  # Pour créer un espace vide après chaque message

if __name__ == '__main__':
    main()